//	Task: Use the let keyword in a for...of loop to iterate over an iterable and log the values


const months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];

// Using the for...of loop with the let keyword
for (let month of months) {
  console.log(month);
}
